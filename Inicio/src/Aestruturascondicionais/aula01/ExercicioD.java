package Aestruturascondicionais.aula01;

public class ExercicioD {

    public static void main(String[] args) {

        double valorA = 5;
        double valorB = 3;

        double soma = valorA + valorB;

        double subtracao = valorA - valorB;

        double multiplicacao = valorA * valorB;

        double divisao = valorA / valorB;

        double restoDivisao = valorA % valorB;

        System.out.println("Soma: " + soma + "\n" +
                "Subtração: " + subtracao + "\n" +
                "Multiplicação: " + multiplicacao + "\n" +
                "Divisão: " + divisao + "\n" +
                "Resto da divisão: " + restoDivisao);
    }
}
